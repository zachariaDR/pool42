int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	while (nb > 1 && i <= nb / 2)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	if (nb < 2)
		return (0);
	return (1);
}

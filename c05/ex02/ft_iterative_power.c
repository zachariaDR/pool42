int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	a;

	if (power < 0)
		return (0);
	else if (power == 0)
		return (1);
	else if (power == 1)
		return (nb);
	else
	{
		i = 1;
		a = nb;
		while (i < power)
		{
			a = a * nb;
			i++;
		}
		return (a);
	}
}
